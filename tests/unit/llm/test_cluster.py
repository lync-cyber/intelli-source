class TestEdgeCases:
    """Boundary conditions and edge cases for cluster processing."""

    def test_empty_body_text(
        self,
        mock_gateway: AsyncMock,
        mock_vector_store_no_match: MagicMock,
        mock_call_log: AsyncMock,
    ) -> None:
        """Processing with empty body_text should not raise."""
        processor = ContentClusterer(
            gateway=mock_gateway,
            vector_store=mock_vector_store_no_match,
            call_log=mock_call_log,
        )
        ctx = PipelineContext()
        ctx.set("title", "Some Title")
        ctx.set("body_text", "")
        ctx.set("embedding", [0.0])
        result = processor.process(ctx)
        assert isinstance(result, PipelineContext)
        assert result.get("cluster_id") is not None

    def test_missing_embedding_raises(
        self,
        mock_gateway: AsyncMock,
        mock_vector_store_no_match: MagicMock,
        mock_call_log: AsyncMock,
    ) -> None:
        """When embedding is missing from context, should raise ValueError or KeyError."""
        processor = ContentClusterer(
            gateway=mock_gateway,
            vector_store=mock_vector_store_no_match,
            call_log=mock_call_log,
        )
        ctx = PipelineContext()
        ctx.set("title", "Some Title")
        ctx.set("body_text", "Some body")
        # No embedding set
        with pytest.raises((ValueError, KeyError)):
            processor.process(ctx)

    def test_vector_store_error_during_search_degrades_gracefully(
        self,
        mock_gateway: AsyncMock,
        mock_call_log: AsyncMock,
        pipeline_context: PipelineContext,
    ) -> None:
        """When vector store search fails, processor should degrade gracefully."""
        failing_store = MagicMock()
        failing_store.find_nearest_cluster = MagicMock(
            side_effect=Exception("Vector store unavailable")
        )
        new_cluster = MagicMock()
        new_cluster.id = "fallback-cluster-001"
        failing_store.create_cluster = MagicMock(return_value=new_cluster)
        failing_store.update_centroid = MagicMock()

        processor = ContentClusterer(
            gateway=mock_gateway,
            vector_store=failing_store,
            call_log=mock_call_log,
        )
        # Should not raise; should create a new cluster as fallback
        result_ctx = processor.process(pipeline_context)
        assert isinstance(result_ctx, PipelineContext)
        assert result_ctx.get("cluster_id") is not None
