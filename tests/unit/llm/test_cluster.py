class TestFallbackLogic:
    """Verify degradation to TF-IDF + cosine similarity when LLM fails."""

    def test_fallback_on_gateway_error_creates_cluster_with_tfidf_topic(
        self,
        mock_gateway_failing: AsyncMock,
        mock_vector_store_no_match: MagicMock,
        mock_call_log: AsyncMock,
        pipeline_context: PipelineContext,
    ) -> None:
        """When LLM fails, fallback should use TF-IDF keywords as topic."""
        processor = ContentClusterer(
            gateway=mock_gateway_failing,
            vector_store=mock_vector_store_no_match,
            call_log=mock_call_log,
        )
        result_ctx = processor.process(pipeline_context)

        # Should still create a cluster (not raise)
        mock_vector_store_no_match.create_cluster.assert_called_once()
        # Fallback indicator should be set
        assert result_ctx.get("cluster_fallback") is True

    def test_fallback_topic_derived_from_tfidf(
        self,
        mock_gateway_failing: AsyncMock,
        mock_vector_store_no_match: MagicMock,
        mock_call_log: AsyncMock,
        pipeline_context: PipelineContext,
    ) -> None:
        """Fallback topic should be derived from TF-IDF keywords, not empty."""
        processor = ContentClusterer(
            gateway=mock_gateway_failing,
            vector_store=mock_vector_store_no_match,
            call_log=mock_call_log,
        )
        result_ctx = processor.process(pipeline_context)

        topic = result_ctx.get("cluster_topic")
        assert topic is not None
        assert isinstance(topic, str)
        assert len(topic) > 0

    def test_fallback_method_indicator(
        self,
        mock_gateway_failing: AsyncMock,
        mock_vector_store_no_match: MagicMock,
        mock_call_log: AsyncMock,
        pipeline_context: PipelineContext,
    ) -> None:
        """Fallback path should set cluster_method to 'tfidf'."""
        processor = ContentClusterer(
            gateway=mock_gateway_failing,
            vector_store=mock_vector_store_no_match,
            call_log=mock_call_log,
        )
        result_ctx = processor.process(pipeline_context)
        assert result_ctx.get("cluster_method") == "tfidf"

    def test_normal_path_method_indicator(
        self,
        clusterer_no_match: ContentClusterer,
        pipeline_context: PipelineContext,
    ) -> None:
        """Normal path (LLM success) should set cluster_method to 'llm'."""
        result_ctx = clusterer_no_match.process(pipeline_context)
        assert result_ctx.get("cluster_method") == "llm"


