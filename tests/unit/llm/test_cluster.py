class TestClusterAssignment:
    """Verify new content joins existing cluster or creates new one."""

    def test_joins_existing_cluster_when_match_found(
        self,
        clusterer_with_match: ContentClusterer,
        pipeline_context: PipelineContext,
    ) -> None:
        """When vector store finds a matching cluster, content joins it."""
        result_ctx = clusterer_with_match.process(pipeline_context)
        assert result_ctx.get("cluster_id") == "cluster-001"

    def test_creates_new_cluster_when_no_match(
        self,
        clusterer_no_match: ContentClusterer,
        pipeline_context: PipelineContext,
        mock_vector_store_no_match: MagicMock,
    ) -> None:
        """When no matching cluster, a new cluster must be created."""
        result_ctx = clusterer_no_match.process(pipeline_context)
        mock_vector_store_no_match.create_cluster.assert_called_once()
        assert result_ctx.get("cluster_id") == "cluster-new-001"

    def test_vector_store_searched_with_embedding(
        self,
        clusterer_with_match: ContentClusterer,
        pipeline_context: PipelineContext,
        mock_vector_store_with_match: MagicMock,
    ) -> None:
        """process() must call find_nearest_cluster with the content embedding."""
        clusterer_with_match.process(pipeline_context)

        mock_vector_store_with_match.find_nearest_cluster.assert_called_once()
        call_args = mock_vector_store_with_match.find_nearest_cluster.call_args
        args, kwargs = call_args
        passed_embedding = args[0] if args else kwargs.get("embedding")
        assert passed_embedding == SAMPLE_EMBEDDING

    def test_multiple_contents_same_cluster(
        self,
        mock_gateway: AsyncMock,
        mock_vector_store_with_match: MagicMock,
        mock_call_log: AsyncMock,
    ) -> None:
        """Multiple related contents should be assigned to the same cluster."""
        processor = ContentClusterer(
            gateway=mock_gateway,
            vector_store=mock_vector_store_with_match,
            call_log=mock_call_log,
        )

        ctx1 = PipelineContext()
        ctx1.set("title", SAMPLE_TITLE)
        ctx1.set("body_text", SAMPLE_BODY)
        ctx1.set("embedding", SAMPLE_EMBEDDING)

        ctx2 = PipelineContext()
        ctx2.set("title", RELATED_TITLE)
        ctx2.set("body_text", RELATED_BODY)
        ctx2.set("embedding", RELATED_EMBEDDING)

        result1 = processor.process(ctx1)
        result2 = processor.process(ctx2)

        # Both should join the same existing cluster
        assert result1.get("cluster_id") == "cluster-001"
        assert result2.get("cluster_id") == "cluster-001"


